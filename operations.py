def add(num_1, num_2):
    result = int(num_1) + int(num_2)
    print(f'{num_1} + {num_2} is equal to {result}')
    return result

def rest(num_1, num_2):
    result = int(num_1) - int(num_2)
    print(f'{num_1} - {num_2} is equal to {result}')
    return result

def multiply(num_1, num_2):
    result = int(num_1) * int(num_2)
    print(f'{num_1} * {num_2} is equal to {result}')
    return result
    
def divide(num_1, num_2):
    result = int(num_1) / int(num_2)
    print(f'{num_1} / {num_2} is equal to {result}')
    return result

def potencia(num_1, num_2):
    result = int(num_1) ** int(num_2)
    print(f'{num_1} ^ {num_2} is equal to {result}')
    return result

def modulo(num_1, num_2):
    result = int(num_1) % int(num_2)
    print(f'{num_1} % {num_2} (residuo) is equal to {result}')
    return result

